color = prompt(String,"Enter a color: ");

switch(color){
    case "Red" :
        console.log("The color is Red");
        break;
    case "Green" :
        console.log("The color is Green");
        break;
    case "Blue" :
        console.log("The color is Blue");
        break;
    default :
        console.log("The color is not Red, Green or Blue"); 
}